package Bucles;

/* Ejercicio 2
Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle while.

@author Miguel Falcón
*/

public class Eje02 {
    public static void main(String[] args) {

        int n=0;

        while(n<=100){
            System.out.print(n+" ");
            n=n+5;
        }

    }
}
